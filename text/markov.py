import markovify
import json
import time


# Generates captions from scraped Tiktok viral captions
# Uses Markov chain model to generate captions
# Found from search results so they are clearly high performing
# TODO - Add a seed for context
# 30% overlap seems to be the lowest it can go. It hangs on 20.
# Average time seems to be ~0.1, 0.2s
class TiktokCaption:

    def __init__(self, caption: str):
        self.caption = caption
        with open("../data/scraped_results.json", "r") as scraped_results:
            self.ds = json(scraped_results)

        return self.generate_caption()

    def generate_caption(self):
        text_model = markovify.NewlineText(self.ds)
        title = text_model.make_sentence(
            max_overlap_ratio=0.3, min_words=11, max_words=23
        )

        return title


